import java.util.*;
public class dot {
	private Integer x;
	private Integer y;
	public Integer getx() {
		return x;
	}
	public void setx(Integer x){
		this.x=x;
	}
	public Integer gety() {
		return y;
	}
	public void sety(Integer y){
		this.y=y;
	}
	public String toString() {
		return x.toString()+ " " + y.toString()+ " " + "";
	}
}